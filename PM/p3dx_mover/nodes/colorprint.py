
class colorprint:
  def red(self, prt): print("\033[91m {}\033[00m" .format(prt))
  def green(self,prt): print("\033[92m {}\033[00m" .format(prt))
  def yellow(self,prt): print("\033[93m {}\033[00m" .format(prt))
  def light_purple(self,prt): print("\033[94m {}\033[00m" .format(prt))
  def purple(self,prt): print("\033[95m {}\033[00m" .format(prt))
  def cyan(self,prt): print("\033[96m {}\033[00m" .format(prt))
  def light_gray(self,prt): print("\033[97m {}\033[00m" .format(prt))
  def black(self,prt): print("\033[98m {}\033[00m" .format(prt))
  def target(self, prt): print("\033[91m {}\033[00m" .format(prt))
  def wander(self,prt): print("\033[94m {}\033[00m" .format(prt))
  def robot(self,prt): print("\033[92m {}\033[00m" .format(prt))
  